local map = require("config.utils").map
map("v", "<", "<gv", { desc = "dedent" })
map("v", ">", ">gv", { desc = "indent" })
map("n", "<TAB>", ":bn<CR>", { desc = "next tab" })
map("n", "<S-TAB>", ":bp<CR>", { desc = "previous tab" })
map("n", "n", "nzzzv", { desc = "next match and center" })
map("n", "N", "Nzzzv", { desc = "previous match and center" })
map("n", "<c-h>", "<c-w><c-h>", { desc = "Move focus to the left window" })
map("n", "<c-l>", "<c-w><c-l>", { desc = "Move focus to the right window" })
map("n", "<c-j>", "<c-w><c-j>", { desc = "Move focus to the lower window" })
map("n", "<c-k>", "<c-w><c-k>", { desc = "Move focus to the upper window" })
map("n", "<C-Up>", ":resize -2<CR>", { desc = "resize up" })
map("n", "<C-Down>", ":resize +2<CR>", { desc = "resize down" })
map("n", "<C-Left>", ":vertical resize -2<CR>", { desc = "resize left" })
map("n", "<C-Right>", ":vertical resize +2<CR>", { desc = "resize right" })
map("n", "<C-d>", "<C-d>zz", { desc = "next page and center" })
map("n", "<C-u>", "<C-u>zz", { desc = "previous page and center" })
map("n", "<A-j>", ":m .+1<CR>==", { desc = "move line up" })
map("n", "<A-k>", ":m .-2<CR>==", { desc = "move line down" })
map("i", "<A-j>", "<Esc>:m .+1<CR>==gi", { desc = "move line up" })
map("i", "<A-k>", "<Esc>:m .-2<CR>==gi", { desc = "move line down" })
map("i", "<A-u>", "<c-r>=trim(system('uuidgen'))<cr>", { desc = "insert uuid at cursor" })
map("n", "<A-u>", "i<c-r>=trim(system('uuidgen'))<cr><esc>", { desc = "insert uuid at cursor" })
